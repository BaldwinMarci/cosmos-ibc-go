// OnRecvPacket implements the IBCMiddleware interface
func (im IBCMiddleware) OnRecvPacket(
	ctx sdk.Context,
	packet channeltypes.Packet,
	relayer sdk.AccAddress,
) ibcexported.Acknowledgement {
	if hook, ok := im.Hooks.(TransferHooksOnRecvPacketOverride); ok {
		return hook.OnRecvPacketOverride(im, ctx, packet, relayer)
	}

	// TODO: Do we want to allow the hooks to return? Maybe short circuit or override the packet or relayer?
	//       ...Or maybe for that users can use the override hook
	if hook, ok := im.Hooks.(TransferHooksOnRecvPacketBefore); ok {
		hook.OnRecvPacketBeforeHook(ctx, packet, relayer)
	}

	ack := im.App.OnRecvPacket(ctx, packet, relayer)

	if hook, ok := im.Hooks.(TransferHooksOnRecvPacketAfter); ok {
		hook.OnRecvPacketAfterHook(ctx, packet, relayer)
	}

	return ack
}

